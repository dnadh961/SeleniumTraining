package com.dev.testng;

public class Maths {

	public int add(int a, int b) {
		return a+b;
	}
	
	public int substract(int a, int b) {
		return a-b;
	}
	
	public int multiply(int a, int b) {
		return a*b;
	}
	
	public int divide(int a, int b) {
		return a/b;
	}
	
	public boolean isEvenNum(int num) {
		return num % 2 == 0;
	}
}
